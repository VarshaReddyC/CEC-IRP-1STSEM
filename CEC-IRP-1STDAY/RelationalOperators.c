#include<stdio.h>
void main()
{
    int n1=20;
    int n2=50;
    printf("%d > %d:%d\n",n1,n2,n1>n2);
    printf("%d < %d:%d\n",n1,n2,n1<n2);
    printf("%d >= %d:%d\n",n1,n2,n1>=n2);
    printf("%d <= %d:%d\n",n1,n2,n1<=n2);
    printf("%d == %d:%d\n",n1,n2,n1==n2);
    printf("%d != %d:%d\n",n1,n2,n1!=n2);
}